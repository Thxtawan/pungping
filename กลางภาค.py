# ข้อ 4.3: รับค่าจนกว่าจะใส่ค่าติดลบ แล้วหาค่ามากที่สุด
print("ข้อ 4.3: หาค่าสูงสุดจากค่าที่รับเข้ามา (หยุดเมื่อเจอค่าติดลบ)")
max_value = None
while True:
    num = float(input("กรอกจำนวน: "))
    if num < 0:
        break
    if max_value is None or num > max_value:
        max_value = num
print("ค่ามากที่สุดที่กรอกคือ:", max_value, "\n")

