# ข้อ 5: รับค่าจำนวนเต็ม 5 ตัวแล้วหาผลรวมและค่าเฉลี่ย
print("ข้อ 5: รับค่าจำนวนเต็ม 5 ตัว หาผลรวมและค่าเฉลี่ย")
numbers = []
for i in range(5):
    num = int(input(f"ตัวเลขที่ {i+1}: "))
    numbers.append(num)
total = sum(numbers)
average = total / 5
print("ผลรวม =", total)
print("ค่าเฉลี่ย =", average)
