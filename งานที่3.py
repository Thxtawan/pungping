# เริ่มต้นด้วยเงิน 1000 บาท
money = 1000.00

# รายการสินค้าและราคาต่อชิ้น
items = {
    "หมูสับกิโล": 150.00,
    "เนื้ออกไก่": 105.00,
    "ไก่บ้าน(ตัว)": 120.00,
    "มาม่าต้มยำ": 6.50,
    "ข้าวสาร": 150.00,
    "น้ำตาล": 20.00,
    "ปลากะป๋องสามแม่ครัว": 10.00,
    "ซอสน้ำมันหอย": 18.00,
    "ผงชูรส": 10.25,
    "ไข่แผงคละเบอร์": 120.25,
    "ชาเขียว": 21.50,
    "Pepsi": 29.50,
    "กาแฟ": 15.75,
    "ขนมปังอบเนย": 19.00,
    "ชาไทย": 11.50
}

# คำนวณราคารวมของทั้งหมด
total = 0
print("รายการสินค้าที่ซื้อ:")
for name, price in items.items():
    print(f"- {name}: {price:.2f} บาท")
    total += price

# คำนวณเงินทอน
change = money - total

# แสดงผล
print("\nรวมราคาสินค้าทั้งหมด:", round(total, 2), "บาท")
print("เงินที่ชำระ:", money, "บาท")
print("เงินทอน:", round(change, 2), "บาท")
